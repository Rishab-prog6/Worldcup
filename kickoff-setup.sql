-- predictions: reject once a result exists OR kickoff has passed
drop policy if exists "insert predictions" on predictions;
create policy "insert predictions" on predictions for insert
with check (
  not exists (select 1 from results r where r.match_id = predictions.match_id and r.home is not null)
  and not exists (select 1 from matches mt where mt.match_id = predictions.match_id and mt.kickoff is not null and now() >= mt.kickoff)
);